package org.oskari.spatineo.serval.api;

public class ServalStats {
    
    private double hoursDown;
    private double hoursMaintenance;
    private double hoursUp;
    
    public double getHoursDown() {
        return hoursDown;
    }
    
    public void setHoursDown(double hoursDown) {
        this.hoursDown = hoursDown;
    }

    public double getHoursMaintenance() {
        return hoursMaintenance;
    }

    public void setHoursMaintenance(double hoursMaintenance) {
        this.hoursMaintenance = hoursMaintenance;
    }

    public double getHoursUp() {
        return hoursUp;
    }

    public void setHoursUp(double hoursUp) {
        this.hoursUp = hoursUp;
    }

}
